#include <iostream>
#include <string>
using namespace std;

int main () {

  int myNum = 5;
  float myFloatNum = 5.99;
  double myDoubleNum = 9.98;
  char myLetter = 'D';
  bool myBoolean = true;
  string myString = "Hello";


  cout << "int: " << myNum << "\n";
  cout << "float: " << myFloatNum << "\n";
  cout << "double: " << myDoubleNum << "\n";
  cout << "char: " << myLetter << "\n";
  cout << "bool: " << myBoolean << "\n";
  cout << "string: " << myString << "\n";

  return 0;
}
